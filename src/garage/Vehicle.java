package garage;

public class Vehicle {

	//instance variables
	
		private int id;
		private String model;
		private String brand;
		boolean isCustom;
		
		
		public Vehicle(int id, String model, String brand, boolean isCustom) {
			super();
			this.id = id;
			this.model = model;
			this.brand = brand;
			this.isCustom = isCustom;
		}
		
		
		
	
}
